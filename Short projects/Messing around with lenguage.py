#Loop code aimed to replace each vowel in a word or phrase with another word using that vowel. This type of game is know as "geringoso" in spanish.

cadena = 'Geringoso'
capadepenapa = ''

for c in cadena:
	if c == "a":
	 capadepenapa = cadena.replace("a", "apa")
	elif c == "e":
	 capadepenapa = cadena.replace("e", "epe") 
	elif c == "i":
	 capadepenapa = cadena.replace("i", "ipi") 
	elif c == "o":
	 capadepenapa = cadena.replace("o", "opo") 
	elif c == "u":
	 capadepenapa = cadena.replace("u", "upu") 
	print(capadepenapa)	
